package com.example.harryschmach.tourguidendab;

import android.app.Fragment;
import android.os.Bundle;
import android.support.design.widget.NavigationView;
import android.support.v4.app.FragmentTransaction;
import android.support.v4.view.GravityCompat;
import android.support.v4.widget.DrawerLayout;
import android.support.v7.app.ActionBarDrawerToggle;
import android.support.v7.app.AppCompatActivity;
import android.support.v7.widget.Toolbar;
import android.view.Menu;
import android.view.MenuItem;

public class MainActivity extends AppCompatActivity
        implements NavigationView.OnNavigationItemSelectedListener, AttractionFragment.OnListFragmentInteractionListener {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Toolbar toolbar = findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);

        DrawerLayout drawer = findViewById(R.id.drawer_layout);
        ActionBarDrawerToggle toggle = new ActionBarDrawerToggle(
                this, drawer, toolbar, R.string.navigation_drawer_open, R.string.navigation_drawer_close);
        drawer.addDrawerListener(toggle);
        toggle.syncState();

        NavigationView navigationView = findViewById(R.id.nav_view);
        navigationView.setNavigationItemSelectedListener(this);

        displaySelectedScreen(R.id.nav_attractions);
    }

    @Override
    public void onBackPressed() {
        DrawerLayout drawer = findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
        }
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }

    // https://www.simplifiedcoding.net/android-navigation-drawer-example-using-fragments/#Android-Navigation-Drawer-Example
    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();
        android.support.v4.app.Fragment fragmentChosen = null;
        Bundle args = new Bundle();


        if (id == R.id.nav_attractions) {
            fragmentChosen = new AttractionFragment();
            args.putInt("whichInt", 0);
            fragmentChosen.setArguments(args);
        } else if (id == R.id.nav_sunsets) {
            fragmentChosen = new AttractionFragment();
            args.putInt("whichInt", 1);
            fragmentChosen.setArguments(args);
        } else if (id == R.id.nav_surfing) {
            fragmentChosen = new AttractionFragment();
            args.putInt("whichInt", 2);
            fragmentChosen.setArguments(args);
        } else if (id == R.id.nav_foods) {
            fragmentChosen = new AttractionFragment();
            args.putInt("whichInt", 3);
            fragmentChosen.setArguments(args);
        }

        if (fragmentChosen != null) {
            FragmentTransaction ft = getSupportFragmentManager().beginTransaction();
            ft.replace(R.id.content_frame, fragmentChosen);
            ft.commit();
        }

        DrawerLayout drawer = findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

    private void displaySelectedScreen(int itemId) {

        //creating fragment object
        android.support.v4.app.Fragment fragment = null;
        Bundle args = new Bundle();


        //initializing the fragment object which is selected
        switch (itemId) {
            case R.id.nav_attractions:
                fragment = new AttractionFragment();
                args.putInt("whichInt", 0);
                fragment.setArguments(args);
                break;
            case R.id.nav_sunsets:
                fragment = new AttractionFragment();
                args.putInt("whichInt", 1);
                fragment.setArguments(args);
                break;
            case R.id.nav_surfing:
                fragment = new AttractionFragment();
                args.putInt("whichInt", 2);
                fragment.setArguments(args);
                break;
            case R.id.nav_foods:
                fragment = new AttractionFragment();
                args.putInt("whichInt", 3);
                fragment.setArguments(args);
                break;
        }

        //replacing the fragment
        if (fragment != null) {
            FragmentTransaction ft = getSupportFragmentManager().beginTransaction();
            ft.replace(R.id.content_frame, fragment);
            ft.commit();
        }

        DrawerLayout drawer = findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
    }

    @Override
    public void onListFragmentInteraction(SDAttraction item) {

    }
}
